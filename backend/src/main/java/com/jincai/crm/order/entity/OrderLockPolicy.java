package com.jincai.crm.order.entity;

/**
 * 订单库存锁定策略枚举。
 *
 * <p>定义订单在何时锁定出团日期库存，是平衡销售灵活性和
 * 库存控制严格性的关键策略配置。
 *
 * <p><b>策略对比</b>：
 * <table border="1">
 *   <tr>
 *     <th>策略</th>
 *     <th>锁定时机</th>
 *     <th>优点</th>
 *     <th>缺点</th>
 *     <th>适用场景</th>
 *   </tr>
 *   <tr>
 *     <td>ON_APPROVAL</td>
 *     <td>审批通过后立即锁定</td>
 *     <td>严格控制库存，防止超卖</td>
 *     <td>可能影响销售转化率</td>
 *     <td>热门线路、限量产品</td>
 *   </tr>
 *   <tr>
 *     <td>ON_DEPOSIT</td>
 *     <td>客户支付定金后锁定</td>
 *     <td>平衡库存与销售，提高转化</td>
 *     <td>存在短暂超卖风险</td>
 *     <td>常规产品、预售产品</td>
 *   </tr>
 *   <tr>
 *     <td>MANUAL</td>
 *     <td>操作员手动锁定</td>
 *     <td>最大灵活性，人工控制</td>
 *     <td>依赖操作员经验，易出错</td>
 *     <td>大客户订单、定制产品</td>
 *   </tr>
 * </table>
 *
 * <p><b>业务规则</b>：
 * <ul>
 *   <li>策略在订单创建时从产品配置继承，可被出团日期配置覆盖</li>
 *   <li>库存不足时，任何策略都无法执行锁定操作</li>
 *   <li>订单取消时，已锁定库存自动释放</li>
 *   <li>订单完成时，锁定库存转为实际消耗</li>
 * </ul>
 *
 * @see TravelOrder#lockPolicy 订单锁定策略字段
 * @see RouteProduct#lockPolicyDefault 线路产品默认锁定策略
 * @see Departure#lockPolicyOverride 出团日期锁定策略覆盖
 */
public enum OrderLockPolicy {
    /**
     * 审批通过后锁定。
     *
     * <p>订单获得最终审批后立即执行库存锁定，是最严格的库存控制策略：
     * <ul>
     *   <li>审批通过时检查库存，库存不足则拒绝审批</li>
     *   <li>严格防止超卖，保证出团确定性</li>
     *   <li>可能影响销售转化率（客户不愿等待审批）</li>
     * </ul>
     *
     * <p>适用场景：
     * <ul>
     *   <li>热门线路或限量产品，需严格控制库存</li>
     *   <li>高价值订单，需审批风控</li>
     *   <li>团队协作流程，审批是必要环节</li>
     * </ul>
     *
     * <p>业务影响：
     * <ul>
     *   <li>审批通过时可能因库存不足而失败</li>
     *   <li>锁定后即使客户不付款也暂不释放（除非手动或超时）</li>
     *   <li>客户需理解"审批通过≠预订成功"的概念</li>
     * </ul>
     */
    ON_APPROVAL,

    /**
     * 支付定金后锁定。
     *
     * <p>客户支付定金后才执行库存锁定，平衡了销售转化和库存控制：
     * <ul>
     *   <li>审批通过时不立即锁定，提高审批通过率</li>
     *   <li>客户支付定金后才实际占用库存，提升转化体验</li>
     *   <li>存在极短暂的超卖窗口期（审批通过到支付定金之间）</li>
     * </ul>
     *
     * <p>适用场景：
     * <ul>
     *   <li>常规旅游产品，库存相对充足</li>
     *   <li>预售产品，需通过定金筛选真实意向客户</li>
     *   <li>希望提高销售转化率的产品线</li>
     * </ul>
     *
     * <p>业务影响：
     * <ul>
     *   <li>审批通过更容易，客户体验更好</li>
     *   <li>需设置定金支付时限，超时自动释放锁定资格</li>
     *   <li>需处理极少数因超卖导致的定金退款情况</li>
     * </ul>
     */
    ON_DEPOSIT,

    /**
     * 签署合同后锁定。
     *
     * <p>客户签署合同后才执行库存锁定，适用于合同签署是最终确认环节的场景：
     * <ul>
     *   <li>审批通过和支付定金都不自动触发锁定</li>
     *   <li>等待客户签署正式合同后才确认订单</li>
     *   <li>最大程度保证订单的确定性和法律效力</li>
     * </ul>
     *
     * <p>适用场景：
     * <ul>
     *   <li>高价值订单，需要合同作为最终保障</li>
     *   <li>出境游、定制游等复杂产品</li>
     *   <li>企业客户要求先审批后走合同流程</li>
     * </ul>
     *
     * <p>业务影响：
     * <ul>
     *   <li>合同签署前库存可能被其他订单占用</li>
     *   <li>客户可能有较长时间考虑，影响库存周转</li>
     *   <li>需要合同管理系统支持电子签约</li>
     * </ul>
     */
    ON_CONTRACT,

    /**
     * 手动锁定。
     *
     * <p>由操作员根据业务需要手动执行库存锁定，提供最大的灵活性：
     * <ul>
     *   <li>审批通过和支付定金都不自动触发锁定</li>
     *   <li>操作员可综合客户等级、付款记录等决定锁定时机</li>
     *   <li>可实现延迟锁定、批量锁定等特殊操作</li>
     * </ul>
     *
     * <p>适用场景：
     * <ul>
     *   <li>大客户或VIP客户的特殊订单</li>
     *   <li>定制化产品，需人工确认细节后锁定</li>
     *   <li>复杂订单，需协调多个资源后才能锁定</li>
     * </ul>
     *
     * <p>业务影响：
     * <ul>
     *   <li>高度依赖操作员的专业判断</li>
     *   <li>可能因遗忘或失误导致库存管理混乱</li>
     *   <li>需建立完善的操作规程和监控机制</li>
     * </ul>
     */
    MANUAL
}