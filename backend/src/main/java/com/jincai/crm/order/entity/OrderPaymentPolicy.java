package com.jincai.crm.order.entity;

/**
 * 订单付款策略枚举。
 *
 * <p>定义订单的付款方式和阶段，直接影响客户的资金流和
 * 旅行社的现金流管理策略。
 *
 * <p><b>策略对比</b>：
 * <table border="1">
 *   <tr>
 *     <th>策略</th>
 *     <th>付款阶段</th>
 *     <th>现金流特点</th>
 *     <th>风险控制</th>
 *     <th>适用场景</th>
 *   </tr>
 *   <tr>
 *     <td>FULL</td>
 *     <td>一次性全额付款</td>
 *     <td>资金快速回笼</td>
 *     <td>客户违约风险高</td>
 *     <td>短途游、周边游</td>
 *   </tr>
 *   <tr>
 *     <td>DEPOSIT_BALANCE</td>
 *     <td>定金+尾款分阶段付款</td>
 *     <td>资金分阶段回笼</td>
 *     <td>平衡风险与转化</td>
 *     <td>长途游、出境游</td>
 *   </tr>
 * </table>
 *
 * <p><b>业务规则</b>：
 * <ul>
 *   <li>策略在订单创建时从产品配置继承，可被出团日期配置覆盖</li>
 *   <li>不同策略对应不同的定金规则和支付时限</li>
 *   <li>策略变更需经审批，防止随意修改影响财务计划</li>
 * </ul>
 *
 * @see TravelOrder#paymentPolicy 订单付款策略字段
 * @see RouteProduct#paymentPolicyDefault 线路产品默认付款策略
 * @see Departure#paymentPolicyOverride 出团日期付款策略覆盖
 * @see DepositRuleType 定金规则类型
 */
public enum OrderPaymentPolicy {
    /**
     * 全额付款策略。
     *
     * <p>要求客户在规定时间内一次性支付订单全额，是最直接的付款方式：
     * <ul>
     *   <li>资金快速回笼，改善旅行社现金流</li>
     *   <li>操作简单，减少财务管理复杂度</li>
     *   <li>客户资金压力大，可能影响销售转化率</li>
     * </ul>
     *
     * <p>付款流程：
     * <ol>
     *   <li>订单审批通过后，生成全额应收款项</li>
     *   <li>客户需在规定时限内支付全额</li>
     *   <li>收到全款后，订单满足出行条件</li>
     * </ol>
     *
     * <p>适用场景：
     * <ul>
     *   <li>短途旅游产品（1-3天），出行日期较近</li>
     *   <li>价格较低的产品，客户支付压力小</li>
     *   <li>企业客户或团体客户，有明确的预算和付款计划</li>
     *   <li>尾款客户，基于信任关系的简化流程</li>
     * </ul>
     *
     * <p>风险控制：
     * <ul>
     *   <li>设置合理的付款时限，避免客户遗忘</li>
     *   <li>提供多种支付渠道，提高支付便利性</li>
     *   <li>建立催款机制，及时跟进未付款订单</li>
     * </ul>
     */
    FULL,

    /**
     * 定金+尾款策略。
     *
     * <p>将订单付款分为定金和尾款两个阶段，是最常用的付款策略：
     * <ul>
     *   <li>降低客户单次支付压力，提高销售转化率</li>
     *   <li>通过定金筛选真实意向客户</li>
     *   <li>分阶段回笼资金，平衡现金流和风险</li>
     * </ul>
     *
     * <p>付款流程：
     * <ol>
     *   <li>订单审批通过后，根据定金规则生成定金应收</li>
     *   <li>客户支付定金后，订单获得"准确认"资格</li>
     *   <li>根据尾款时限，提醒客户支付尾款</li>
     *   <li>收到尾款后，订单满足最终出行条件</li>
     * </ol>
     *
     * <p>适用场景：
     * <ul>
     *   <li>长途旅游产品（4天以上），需提前较长时间预订</li>
     *   <li>出境旅游产品，涉及签证等复杂流程</li>
     *   <li>价格较高的产品，客户需分期安排资金</li>
     *   <li>新客户或信用记录不明确的客户</li>
     * </ul>
     *
     * <p>风险控制：
     * <ul>
     *   <li>合理设置定金比例（通常10%-30%）和金额上限</li>
     *   <li>明确尾款支付时限（通常出行前7-15天）</li>
     *   <li>建立定金不退、尾款逾期取消等规则</li>
     *   <li>对多次逾期客户建立黑名单机制</li>
     * </ul>
     */
    DEPOSIT_BALANCE
}